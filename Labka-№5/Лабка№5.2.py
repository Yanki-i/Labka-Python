'''
Дано два натуральних числа x і y.
Знайти число, у якого середнє арифметичне цифр є більшим.
'''
#Позначення
'''
x-int
y-int
'''
#Введення
x = int(input('Введіть x= '))
y = int(input('Введіть y= '))
#Обчислення
x = x//2-y
y = y//2-x
if x >= y:
    print('Середне арифметичне x є більшим')
else:
    print('Середнє арифметичне y є більшим')
